
public class Point {

	public int x, y;
	
	// Setter for x coordinate
	public void setX (int x) {
		this.x = x;
	}
	
	// Getter for x coordinate
	public int getX () {
		return x;
	}
	
	// Setter for y coordinate
	public void setY (int y) {
		this.y = y;
	}
	
	// Getter for y coordinate
	public int getY () {
		return y;
	}
	
	// Constructor
	public Point() {
		
	}
	
	// Constructor
	public Point(int x, int y) {
		this.x = x;
		this.y = y;
	}
}
